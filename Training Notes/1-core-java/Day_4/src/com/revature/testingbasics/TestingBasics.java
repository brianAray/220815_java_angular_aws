package com.revature.testingbasics;

public class TestingBasics {

	/*
	 * Software Testing
	 * Software testing is a method to check whether the actual software product matches the expected
	 * requirements and to ensure that the product is defect free.
	 * It involves the execution of software / system components using manual or automated tools to
	 * evaluate one or more properties of interest.
	 * 
	 * The purpose is to identify errors, gaps, or missing requirements to actual requirements.
	 * 
	 * An example is Unit Testing
	 * 
	 * Unit Testing is a type of software testing in which individual units or components of code is tested
	 * The purpose is to validate that each unit of the software performs as expected.
	 * Unit testing is usually done duirng the development phase of an application by the developers
	 * Unit tests isolate a section of code and verify it functionality.
	 * 
	 * 
	 * Testing Levels
	 * 
	 * There are about 4 levels
	 * 
	 * - Unit test
	 * 		Test Individual Component
	 * - Integration Test
	 * 		Test Components together
	 * - System Test
	 * 		Test the entire System
	 * - Acceptance Test
	 * 		Test the final system
	 * 
	 * 
	 * 
	 * Software Quality
	 * 
	 * SOftware quality is defined as af ield of study and practice that describes the desirable attributes
	 * of a software.
	 * There are two main approaches: defect management and quality attributes
	 * 
	 * A software defect can be regarded as any failure to address end user requirements
	 * 
	 * Common defects:
	 * - Missed requirements
	 * - Misunderstood requirements
	 * - Errors in design
	 * - Function logic error
	 * - Bad data relationships
	 * - Coding Errors
	 * 
	 * 1 - Functional Suitability
	 * 2 - Reliability
	 * 3 - Operability
	 * 4 - Performance Efficiency
	 * 5 - Security
	 * 6 - Compatibility
	 * 7 - Maintainability
	 * 8 - Transferability
	 * 
	 */
}

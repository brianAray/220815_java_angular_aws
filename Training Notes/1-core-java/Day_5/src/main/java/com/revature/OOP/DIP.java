package com.revature.OOP;

public class DIP {

	/*
	 * Dependency Inversion Principle
	 * 
	 * DIP means, high level and low level modules should be decoupled in such a way that changing or replacing
	 * low level modules doesn't require too much work of the high level modules.
	 * 
	 * Both low level and high level modules shouldn't depend on each other, instead depending on abstractions
	 * like interfaces.
	 * 
	 * Abstractions should not depend on details. Details (concrete implementations) should depend on abstractions
	 * 
	 * This decouples modules, makes systems less complex, easier to maintain and update, easier to test, reuse.
	 * 
	 * This closely relates to dependency injection, whihc is the practical implementation. 
	 * The goal of DI (Dependency Injection) is if two classes are dependent on eahc other, their features should
	 * be abstracted away from each other and they should depend on the abstractions of each other.
	 * 
	 * 
	 */
	
	
}

package com.revature.OOP;

public class OpenClosed {
	
	/*
	 * The Open/Closed Principle
	 * 
	 * This principle states that classes or objects and methods should be open for extension but closed
	 * for modification
	 * 
	 * This means that you should design your classes and modules with possible future updates in mind
	 * They should have a generic design that you won't need to change the class itself in order to extend
	 * the behaviour
	 * 
	 * You can add more fields or methods, but in such a way that you don't need to rewrite old methods,
	 * delete old fields and modify the old code in order to make it work again.
	 * Think ahead and you will write stable code, before and after any updates to a requirement
	 * 
	 * This is key in ensuring backwards compatibility and prevent any regressions
	 * Regressions are bugs that happen when your programs features or efficiency breaks after an update
	 */

}

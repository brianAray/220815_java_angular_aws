package com.revature.OOP;

public class LSP {
	
	/*
	 * Liskov Substitution Principle
	 * 
	 * This principle means, any derived classes should be able to subsitute their base class without
	 * behaviour of your code changing
	 * 
	 * This closely relates to the Interface Segregation Principle and the Single Responsibility Principle
	 * Violating any of those will most likely violate LSP.
	 * If a class does more than one thing, subclasses extending it are less liekly to meaningfully 
	 * implement those two or more functionalities
	 * 
	 * A common way to think about object relationships is that there needs to be an is relationship between
	 * classes
	 * 
	 * Think:
	 * 
	 * - Car is a Vehicle
	 * - TeachingAssistant is a CollegeEmployee
	 * 
	 * It is important to note that these relationships do not go both direction. The fact that a car is a
	 * vehicle might not mean that Vehicle is a Car, could be a MotorCycle
	 * 
	 */

}



















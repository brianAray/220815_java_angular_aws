package com.revature.monday;

public class MapInterface {
	
	/*
	 * Map interface used to represent mapping between a key and a value
	 * 
	 * It is not a subtype of the collection interface
	 * 
	 * It behaves differently
	 * 
	 * Maps are perfect for use in key-value association like a dictionary
	 * 
	 * Perform lookups by keys or when someone wants to retrieve and update elements by keys
	 * 
	 * Common scenarios
	 * 
	 * Map of error codes and their descriptions
	 * 
	 * Map of zip codes and cities
	 * 
	 * Map of managers and employees
	 * 		Manager (Key) is associated with a list of employees (values)
	 * 
	 * Map of classes and students
	 * 
	 * A map cannot contain duplicate keys and each key can map to at most one value
	 * Some implementation allow a null key, for example the HashMap and LinkedHashMap, but others do not like TreeMap
	 * 
	 * The order of a map depends on the specific implementations. For example, treeMap and LinkedHashMap have a predicatble
	 * order while HashMap does not
	 */

}

package com.revature.tuesday.comparable;

public class ComparableInterface {

	/*
	 * Comparable Interface
	 * 
	 * This interface is used to order th objects of the user defined class
	 * It only has one method, compareTo(Object o)
	 * 
	 * It provides a single sorting sequence only, you can sort the elements on the basis of a single data member only
	 * 
	 * 
	 * The method itself, is used to compare the current object with the specified object, it returns an integer
	 * 
	 * Postive Integer - Current object is greater
	 * Negative Integer - Current Object is lesser
	 * Zero - They are the same
	 */
	
}
